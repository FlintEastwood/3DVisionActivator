////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ShutterGlasses
{
public:
	ShutterGlasses()
		: leftEye(true)
	{
	}
	
	void toggleEyes()
	{
		leftEye = !leftEye;
	}

	bool isLeftEye() const
	{
		return leftEye;
	}

private:
	bool leftEye;
};

